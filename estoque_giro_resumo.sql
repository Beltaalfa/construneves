-- Agregados sobre GIRO. Executar após estoque_giro_fragment.sql (mesma sessão / concatenação).
SELECT
    COUNT(*) AS TOTAL_PRODUTOS,
    SUM(CASE WHEN G.QUANT_ESTOQUE < 0 THEN 1 ELSE 0 END) AS QTD_SALDO_NEGATIVO,
    SUM(CASE WHEN G.CATEGORIA_ACAO = 'Produtos para Promocao' THEN 1 ELSE 0 END) AS PRODUTOS_PROMOCAO,
    SUM(CASE WHEN G.CATEGORIA_ACAO = 'Produtos para Reposicao' THEN 1 ELSE 0 END) AS PRODUTOS_REPOSICAO,
    SUM(CASE WHEN G.CATEGORIA_ACAO = 'Produtos Parados' THEN 1 ELSE 0 END) AS PRODUTOS_PARADOS,
    SUM(CASE WHEN G.CATEGORIA_ACAO = 'Estavel' THEN 1 ELSE 0 END) AS PRODUTOS_ESTAVEL,
    SUM(CASE WHEN G.STATUS_COBERTURA = 'EXCESSO' THEN 1 ELSE 0 END) AS COBERTURA_EXCESSO,
    SUM(CASE WHEN G.STATUS_COBERTURA = 'CRITICO' THEN 1 ELSE 0 END) AS COBERTURA_CRITICO,
    SUM(CASE WHEN G.STATUS_COBERTURA = 'ALERTA' THEN 1 ELSE 0 END) AS COBERTURA_ALERTA,
    SUM(CASE WHEN G.STATUS_COBERTURA = 'ESTAVEL' THEN 1 ELSE 0 END) AS COBERTURA_ESTAVEL,
    SUM(CASE WHEN G.STATUS_COBERTURA = 'SEM GIRO' THEN 1 ELSE 0 END) AS COBERTURA_SEM_GIRO,
    ROUND(AVG(G.ESTOQUE_EM_DIAS), 2) AS MEDIA_ESTOQUE_EM_DIAS,
    SUM(CASE WHEN G.MARGEM_BRUTA_PCT IS NOT NULL AND G.MARGEM_BRUTA_PCT < 20 THEN 1 ELSE 0 END) AS PRODUTOS_MARGEM_LT_20,
    SUM(CASE WHEN G.SUGESTAO_COMPRA_QTD > 0 THEN 1 ELSE 0 END) AS PRODUTOS_COM_SUGESTAO_COMPRA,
    ROUND(COALESCE(SUM(G.VALOR_ESTOQUE_ESTIMADO), 0), 2) AS TOTAL_VALOR_ESTOQUE_ESTIMADO,
    ROUND(COALESCE(SUM(G.VALOR_VENDAS), 0), 2) AS TOTAL_VALOR_VENDAS_JANELA,
    ROUND(COALESCE(SUM(G.CMV_ESTIMADO), 0), 2) AS TOTAL_CMV_JANELA,
    SUM(CASE WHEN G.FAIXA_MARGEM = '< 20%' THEN 1 ELSE 0 END) AS FAIXA_MARGEM_LT20_COUNT,
    SUM(CASE WHEN G.FAIXA_MARGEM = '20% - 40%' THEN 1 ELSE 0 END) AS FAIXA_MARGEM_20_40_COUNT,
    SUM(CASE WHEN G.FAIXA_MARGEM = '40% - 60%' THEN 1 ELSE 0 END) AS FAIXA_MARGEM_40_60_COUNT,
    SUM(CASE WHEN G.FAIXA_MARGEM = '>= 60%' THEN 1 ELSE 0 END) AS FAIXA_MARGEM_60_PLUS_COUNT,
    SUM(CASE WHEN G.FAIXA_MARGEM = 'SEM VENDA' THEN 1 ELSE 0 END) AS FAIXA_SEM_VENDA_COUNT
FROM GIRO G
